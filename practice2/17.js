const month = 'mar'
switch (month) {
    case 'jan':
        console.log("jan");
        break;
    case 'feb':
        console.log("feb");
        break;
    case 'mar':
        console.log("mar");
        break;
    case 'apr':
        console.log("apr");
        break;

    default:
        console.log('default case match');
        break;
}